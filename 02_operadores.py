#OPERADORES
print (3 + 4)
print (3 - 4)
print (3 * 4)
print (3 / 4)
print (10 % 3) #Muestra el resto de una division
print (10 // 3) # Redondeo de una dividon
print (2 ** 3)# dos elevado a 3

print("hola" + "Python ") # se utiliza para concadenar cadena de textos
print("Hola"+ str(5)) #concatenar texto con numero.
print("hola "*5)# se escribe hola 5 veces


####OPERADORES COMPARATIVOS####

print(3 > 4)
print(3 < 4)
print(3 >= 4)
print(3 <= 4)
print(3 == 4)
print(3 != 4)

#OPERADORES LOGICOS

print (3 > 4 and 8 > 4)
print (3 > 4 or 8 > 4)
print (not(3 > 4) ) #nos dice si la condicion es mentira nos devuelve verdadero